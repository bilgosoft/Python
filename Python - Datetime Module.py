# Datetime Module : Tarih ve zaman kavramları üzerinde çalışmamızı sağlayan modüldür.

import datetime

print(datetime.datetime.now())          # Saat ve tarihi gösterir.
print(datetime.datetime.now().time())   # Sistem tam saati gösterir
print(datetime.datetime.now().hour)     # Sistem saatini gösterir
print(datetime.datetime.now().minute)   # Sistem dakikasını gösterir
print(datetime.datetime.now().second)   # Sistem saniyesini gösterir
print(datetime.datetime.now().year)     # Sistem yılını gösterir
print(datetime.datetime.now().day)      # Sistem gününü gösterir
print(datetime.datetime.now().month)    # Sistem ayını gösterir
print(datetime.datetime.now().year)     # Sistem yılını gösterir