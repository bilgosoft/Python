# Değişken Nedir?
# İngilizcede variable olarak adlandırılan değişkenler bellek üzerinde tutulmasını istediğimiz verileri tutmamızı sağlayan yapıdır.

# Değişken Nasıl Tanımlanır?
# Değişken tanımlamak için önce bir isim vermemiz gerkiyor bu ismi verebilmemiz için aşağıdaki değişken tanımlama kurallarına göz atınız.
# Değişken tanımlama örneği : DegiskenIsmi = DegiskenDegeri

# Değişken Tanımlama Kuralları
# 1. Türkçe karakter kullanılamaz.(Değişken tanımlamalarının ingilizce olarak tanımlanması daha uygundur.)
# 2. Bir değişken sayı ile bitebilir. Ama sayı ile başlayamaz
# 3. Değişken isimlerinde özel karakterler(?, =, vb.) kullanılamaz.

# Değişken Türleri Nelerdir?

# 1. String Variable : Metinsel veya matematiksel işlem yapılamayan sayılar(Telefon, Kimlik Numarası vb.) için kullanılan değişken türüdür.
# Not : String değişken tanımlarken '' , "" , """""" tırnak ifadelerinin arasına değişken değerimizi yazmalıyız.

city = 'İstanbul'
city = "İstanbul"
city = """İstanbul"""

# 2. Integer Variable : Negatif, nötr ve pozitif olmak üzere tam sayı değerindeki verilerimizi tutmak için kullandığımız değişken tipleridir.

number1 = -5
number2 = 0
number3 = 5

# 3. Float Variable : Ondalıklı sayıları tutmamızı sağlayan değişken türleridir.
# Not : Float veri oluştururken sayılar arasında (,) değil (.) kullanırız.

number1 = -5.5
number2 = 0.0
number3 = 5.5

# 4. Bool Variable : Mantıksal veri tutmamızı sağlayan değişken tipidir.
# Not : Sadece 2 adet veri tutabilir.

bool1 = True
bool2 = False