# STRING FORMAT : Bir ifadeyi başka bir ifadeler ile birleştirerek baskı almamızı sağlayan yapıdır.

name = "Ali"

# 1. Yöntem
print("My name is ", name)  

# 2. Yöntem             
print("My name is " + name)      

# 3. Yöntem
print("My name is {0}".format(name))     

# 4. Yöntem
print("My name is {N}".format(N = name)) 

# 5. Yöntem
print(f"My name is {name}")              